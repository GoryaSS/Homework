let yourNumber = prompt('Enter the number and get the symbol', 0);
let massage = (yourNumber == 1) ? '!' :
    (yourNumber == 2) ? '@' :
    (yourNumber == 3) ? '#' :
    (yourNumber == 4) ? '$' :  
    (yourNumber == 5) ? '%' :
    (yourNumber == 6) ? '^' :
    (yourNumber == 7) ? '&' :
    (yourNumber == 8) ? '*' :
    (yourNumber == 9) ? '(' :
    ')' ;
alert(massage);

